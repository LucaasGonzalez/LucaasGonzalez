a = int(input("Entre com o prímeiro valor: "))

b = int(input("Entre com o segundo valor: "))

if a > b:
    
    print ("O prímeiro valor é maior.")
    
if b > a:
    
    print ("O segundo valor é maior.")
    
if a == b:
    
    print ("Os números são iguais.")
